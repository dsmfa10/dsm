// ------------------------------------------------------------------
// Bitcoin HTLC variant – spot-check all four languages emit the type
// ------------------------------------------------------------------
#[test]
fn test_bitcoin_htlc_variant_all_languages() -> Result<(), Box<dyn std::error::Error>> {
    use dsm_gen::schema::{BitcoinHtlcSpec, BitcoinNetwork};

    let vault_spec = VaultSpecification {
        name: "BtcHtlcVault".to_string(),
        version: "1.0.0".to_string(),
        description: None,
        fulfillment_condition: FulfillmentConditionSpec::BitcoinHtlc(BitcoinHtlcSpec {
            hash_lock: "HASHLOCK11111111111111111111111111111111111111111111".to_string(),
            refund_hash_lock: "REFUNDLOCK111111111111111111111111111111111111111".to_string(),
            refund_iterations: 100,
            bitcoin_pubkey: "BTCPUBKEY1111111111111111111111111111111111111111111".to_string(),
            expected_btc_amount_sats: 100_000,
            network: BitcoinNetwork::Testnet,
            min_confirmations: 100,
        }),
        assets: vec![],
        tick_lock: None,
        recovery: None,
        metadata: None,
    };
    let spec = DsmSpecification::Vault(vault_spec);

    let rs = RustGenerator::new(false, false).generate(&spec)?;
    assert!(rs.contains("BitcoinHtlc"));
    assert!(!rs.contains("duration_seconds"));

    let ts = TypeScriptGenerator::new(false, false).generate(&spec)?;
    assert!(ts.contains("bitcoin_htlc"));

    let kt = KotlinGenerator::new(false, false).generate(&spec)?;
    assert!(kt.contains("BitcoinHtlc"));

    let sw = SwiftGenerator::new(false, false).generate(&spec)?;
    assert!(sw.contains("bitcoinHtlc"));
    Ok(())
}
