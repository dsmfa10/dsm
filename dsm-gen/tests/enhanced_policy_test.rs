// ------------------------------------------------------------------
// Policy generation tests (existing + extended)
// ------------------------------------------------------------------
#[test]
fn test_enhanced_policy_client_generation() {
    let policy_spec = PolicySpecification::example("TestPolicy".to_string());
    let spec = DsmSpecification::Policy(policy_spec);
    let generator = RustGenerator::new(false, false);

    let generated_code = match generator.generate(&spec) {
        Ok(code) => code,
        Err(err) => panic!("policy generation failed: {err}"),
    };

    assert!(generated_code.contains("PolicyConfig"));
    assert!(generated_code.contains("TransferRule"));
    assert!(generated_code.contains("RuleCondition"));
    assert!(generated_code.contains("ConditionType"));
    assert!(generated_code.contains("RuleAction"));
    assert!(generated_code.contains("PolicyEvaluationResult"));
    assert!(generated_code.contains("TransactionContext"));
    assert!(generated_code.contains("PolicyClient"));
    assert!(generated_code.contains("config: PolicyConfig"));
    assert!(generated_code.contains("evaluate_transaction"));
    assert!(generated_code.contains("evaluate_amount_limit"));
    assert!(generated_code.contains("evaluate_iteration_window"));
    assert!(generated_code.contains("evaluate_whitelist"));
    assert!(generated_code.contains("evaluate_blacklist"));
    assert!(generated_code.contains("evaluate_signature_required"));
    assert!(generated_code.contains("evaluate_custom_condition"));

    // Invariant #2: no serde_json in generated code
    assert!(!generated_code.contains("serde_json"));
    // Invariant #4: no wall-clock
    assert!(!generated_code.contains("duration_seconds"));
    assert!(!generated_code.contains("time_window_hours"));
}

