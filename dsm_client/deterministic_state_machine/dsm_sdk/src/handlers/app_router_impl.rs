/// Minimal resolved counterparty info used by the contact QR flow.
pub(crate) struct ResolvedCounterparty {
    pub device_id: [u8; 32],
    pub genesis_hash: [u8; 32],
    pub genesis_payload: Vec<u8>,
    pub signing_public_key: Vec<u8>,
    pub verifying_nodes: Vec<dsm::types::identifiers::NodeId>,
}

/// Resolve a ContactQrV3 into a ResolvedCounterparty.
/// This is a minimal, best-effort resolver: it validates the QR payload and
/// returns base32(device_id) and genesis hash for contact lookups.
pub(crate) async fn resolve_counterparty_via_transport(
    qr: &generated::ContactQrV3,
) -> Result<ResolvedCounterparty, String> {
    // device_id is raw 32 bytes on the wire (proto bytes field)
    if qr.device_id.len() != 32 {
        return Err(format!(
            "ContactQrV3: device_id must be 32 bytes (got {})",
            qr.device_id.len()
        ));
    }
    let mut device_id = [0u8; 32];
    device_id.copy_from_slice(&qr.device_id[..32]);

    // genesis_hash is raw 32 bytes on the wire (proto bytes field)
    if qr.genesis_hash.is_empty() {
        return Err("ContactQrV3: genesis_hash missing".to_string());
    }
    if qr.genesis_hash.len() != 32 {
        return Err(format!(
            "ContactQrV3: genesis_hash must be 32 bytes (got {})",
            qr.genesis_hash.len()
        ));
    }
    let mut genesis_hash = [0u8; 32];
    genesis_hash.copy_from_slice(&qr.genesis_hash[..32]);

    let signing_public_key = if qr.signing_public_key.is_empty() {
        return Err("ContactQrV3: signing_public_key missing".to_string());
    } else if qr.signing_public_key.len() != 64 {
        return Err(format!(
            "ContactQrV3: signing_public_key must be 64 bytes (got {})",
            qr.signing_public_key.len()
        ));
    } else {
        qr.signing_public_key.clone()
    };

    let verifying_nodes = verify_device_tree_evidence_quorum(device_id, genesis_hash).await?;

    // At minimum, return arrays and leave payload empty (resolved via fetch later)
    Ok(ResolvedCounterparty {
        device_id,
        genesis_hash,
        genesis_payload: Vec::new(),
        signing_public_key,
        verifying_nodes,
    })
}

async fn verify_device_tree_evidence_quorum(
    device_id: [u8; 32],
    genesis_hash: [u8; 32],
) -> Result<Vec<dsm::types::identifiers::NodeId>, String> {
    // Load the network nodes this device is connected to
    let node_urls = match crate::sdk::storage_node_sdk::StorageNodeConfig::from_env_config().await {
        Ok(cfg) => cfg.node_urls,
        Err(e) => return Err(format!("Failed to load network nodes: {e}")),
    };

    if node_urls.len() < 3 {
        return Err(format!(
            "Insufficient network nodes configured (need >=3, got {})",
            node_urls.len()
        ));
    }

    if std::env::var("DSM_SDK_TEST_MODE").is_ok() {
        let mut verified = Vec::new();
        let mut seen = std::collections::HashSet::new();

        for node_url in &node_urls {
            let trimmed = node_url.trim_end_matches('/');
            if trimmed.is_empty() {
                continue;
            }
            if !seen.insert(trimmed.to_string()) {
                continue;
            }
            verified.push(dsm::types::identifiers::NodeId::new(trimmed.to_string()));
            if verified.len() >= 3 {
                break;
            }
        }

        if verified.len() < 3 {
            return Err(format!(
                "Insufficient network nodes (need >=3, got {})",
                verified.len()
            ));
        }

        return Ok(verified);
    }

    let mut evidence = Vec::with_capacity(32 + 32 + 32 + 4);
    evidence.extend_from_slice(&device_id);
    evidence.extend_from_slice(&genesis_hash);
    evidence.extend_from_slice(&[0u8; 32]);
    evidence.extend_from_slice(&[0u8; 4]);

    let addr = registry_content_addr_b64url(&evidence);
    let client = crate::sdk::storage_node_sdk::build_ca_aware_client();
    let mut verified = Vec::new();
    let mut seen = std::collections::HashSet::new();

    for node_url in &node_urls {
        let trimmed = node_url.trim_end_matches('/');
        if trimmed.is_empty() {
            continue;
        }
        if !seen.insert(trimmed.to_string()) {
            continue;
        }

        let url = format!("{}/api/v2/registry/get/{}", trimmed, addr);
        let resp = match client.get(&url).send().await {
            Ok(r) => r,
            Err(e) => {
                log::warn!(
                    "genesis verify: registry fetch failed for {}: {}",
                    trimmed,
                    e
                );
                continue;
            }
        };

        if !resp.status().is_success() {
            continue;
        }

        let bytes = match resp.bytes().await {
            Ok(b) => b,
            Err(e) => {
                log::warn!(
                    "genesis verify: registry read failed for {}: {}",
                    trimmed,
                    e
                );
                continue;
            }
        };

        if bytes.as_ref() == evidence.as_slice() {
            verified.push(dsm::types::identifiers::NodeId::new(trimmed.to_string()));
        } else if let Ok(entry) = generated::DeviceTreeEntry::decode(bytes.as_ref()) {
            if entry.device_id == device_id && entry.genesis_hash == genesis_hash {
                verified.push(dsm::types::identifiers::NodeId::new(trimmed.to_string()));
            }
        }

        if verified.len() >= 3 {
            break;
        }
    }

    if verified.len() < 3 {
        return Err(format!(
            "Insufficient verified network nodes (need >=3, got {})",
            verified.len()
        ));
    }

    Ok(verified)
}

#[inline]
fn registry_content_addr_b64url(body: &[u8]) -> String {
    let mut hasher = dsm_domain_hasher("DSM/registry");
    hasher.update(body);
    let out = hasher.finalize();
    b64_url_no_pad(out.as_bytes())
}

#[inline]
fn b64_url_no_pad(input: &[u8]) -> String {
    const T: &[u8; 64] = b"ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789-_";
    let n = input.len();
    let mut out = String::with_capacity(n.div_ceil(3) * 4);

    let mut i = 0;
    while i + 3 <= n {
        let a = input[i];
        let b = input[i + 1];
        let c = input[i + 2];

        out.push(T[(a >> 2) as usize] as char);
        out.push(T[(((a & 0x03) << 4) | (b >> 4)) as usize] as char);
        out.push(T[(((b & 0x0f) << 2) | (c >> 6)) as usize] as char);
        out.push(T[(c & 0x3f) as usize] as char);

        i += 3;
    }

    match n - i {
        1 => {
            let a = input[i];
            out.push(T[(a >> 2) as usize] as char);
            out.push(T[((a & 0x03) << 4) as usize] as char);
        }
        2 => {
            let a = input[i];
            let b = input[i + 1];
            out.push(T[(a >> 2) as usize] as char);
            out.push(T[(((a & 0x03) << 4) | (b >> 4)) as usize] as char);
            out.push(T[((b & 0x0f) << 2) as usize] as char);
        }
        _ => {}
    }

    out
}

