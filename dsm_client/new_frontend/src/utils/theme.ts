export function applyTheme(name: ThemeName) {
  const vars = palettes[name];
  if (!vars) {
    console.warn(`[applyTheme] Invalid theme name: ${name}; falling back to stateboy`);
    applyTheme('stateboy');
    return;
  }
  // Set CSS variables directly on <html> so every element inherits them —
  // this is how the original working theme system operated.
  const root = document.documentElement as HTMLElement;
  for (const [k, v] of Object.entries(vars)) {
    root.style.setProperty(k, v);
  }
  // Sync nav backlight to the active theme's dominant colour so the lit icon
  // glow always matches whatever palette is selected (purple → purple glow, etc.)
  root.style.setProperty('--nav-lit-color', vars['--bg'] ?? '#9bbc0f');
  // Also set data-theme attribute for any CSS selectors that depend on it.
  const shell = document.querySelector('.stateboy') as HTMLElement | null;
  const appRoot = document.getElementById('dsm-app-root');
  if (shell) shell.setAttribute('data-theme', name);
  if (appRoot) appRoot.setAttribute('data-theme', name);

  // System bars are permanently near-black (#0D0D0D), set in Android onCreate().
  // No bridge call needed — bars don't change with themes.
}

export function getAvailableThemes(): ThemeName[] {
  return ['stateboy', 'pocket', 'light', 'berry', 'grape', 'dandelion', 'orange', 'teal', 'kiwi', 'greyscale', 'inverted', 'crimson'];
}
