/// Ensure the inbox entry is actually targeted to this device.
/// Validates both the envelope-recipient (base32 text) and the Operation.to_device_id bytes.
pub(crate) fn ensure_inbox_recipient_targets_local(
    entry_recipient_b32: &str,
    op_recipient_bytes: &[u8],
    local_device_bytes: &[u8],
) -> Result<(), String> {
    if local_device_bytes.len() != 32 {
        return Err("local device_id must be 32 bytes".to_string());
    }
    if op_recipient_bytes.len() != 32 {
        return Err("operation.to_device_id must be 32 bytes".to_string());
    }

    let entry_recipient_bytes = crate::util::text_id::decode_base32_crockford(entry_recipient_b32)
        .ok_or_else(|| "recipient_device_id is not valid base32".to_string())?;
    if entry_recipient_bytes.len() != 32 {
        return Err(format!(
            "recipient_device_id decoded length {} != 32",
            entry_recipient_bytes.len()
        ));
    }

    if entry_recipient_bytes.as_slice() != local_device_bytes {
        return Err("recipient_device_id does not target this device".to_string());
    }

    if op_recipient_bytes != local_device_bytes {
        return Err("operation.to_device_id does not target this device".to_string());
    }

    Ok(())
}

fn compute_initial_relationship_chain_tip(
    local_device_id: [u8; 32],
    local_genesis: [u8; 32],
    remote_device_id: &[u8],
    remote_genesis: &[u8],
) -> Option<[u8; 32]> {
    if remote_device_id.len() != 32 || remote_genesis.len() != 32 {
        return None;
    }

    let mut remote_device = [0u8; 32];
    remote_device.copy_from_slice(remote_device_id);
    let mut remote_genesis_arr = [0u8; 32];
    remote_genesis_arr.copy_from_slice(remote_genesis);

    let (genesis_a, device_a, genesis_b, device_b) = if local_device_id < remote_device {
        (
            local_genesis,
            local_device_id,
            remote_genesis_arr,
            remote_device,
        )
    } else {
        (
            remote_genesis_arr,
            remote_device,
            local_genesis,
            local_device_id,
        )
    };

    let mut hasher = dsm::crypto::blake3::dsm_domain_hasher("DSM/bilateral-session");
    hasher.update(&genesis_a);
    hasher.update(&device_a);
    hasher.update(&genesis_b);
    hasher.update(&device_b);

    let mut tip = [0u8; 32];
    tip.copy_from_slice(hasher.finalize().as_bytes());
    Some(tip)
}

pub(crate) fn relationship_tip_for_contact_restore(
    local_device_id: [u8; 32],
    local_genesis: [u8; 32],
    contact: &crate::storage::client_db::ContactRecord,
) -> Option<[u8; 32]> {
    if let Some(stored_tip) = contact.current_chain_tip.as_deref() {
        if stored_tip.len() == 32 {
            let mut tip = [0u8; 32];
            tip.copy_from_slice(stored_tip);
            return Some(tip);
        }
    }

    compute_initial_relationship_chain_tip(
        local_device_id,
        local_genesis,
        &contact.device_id,
        &contact.genesis_hash,
    )
}

/// Route freshness tag for stale-route delivery fallback.
/// `Current` = derived from the contact's current chain tip.
/// `PreviousTip` = derived from the predecessor tip (bounded lookback of 1).
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub(crate) enum RouteFreshness {
    Current,
    PreviousTip,
}

/// A B0x inbox address tagged with its derivation freshness.
#[derive(Debug, Clone)]
pub(crate) struct TaggedInboxAddress {
    pub address: String,
    pub freshness: RouteFreshness,
}

/// Collect per-contact B0x addresses tagged with route freshness.
///
/// Returns up to TWO addresses per contact:
/// 1. Current tip address (always, tagged `Current`)
/// 2. Previous tip address (if stored, non-zero, and different from current; tagged `PreviousTip`)
///
/// This enables stale-route delivery: messages sent to a previous tip's address
/// become visible but are NOT auto-applied to wallet state.
pub(crate) fn collect_tagged_inbox_addresses(
    local_genesis: [u8; 32],
    local_device_id: [u8; 32],
    contacts: &[crate::storage::client_db::ContactRecord],
) -> Vec<TaggedInboxAddress> {
    if local_genesis == [0u8; 32] {
        log::warn!(
            "[collect_tagged_inbox_addresses] genesis is ZERO — returning empty address list"
        );
        return Vec::new();
    }

    let mut addresses = Vec::with_capacity(contacts.len() * 2);

    for contact in contacts {
        let restored_tip =
            relationship_tip_for_contact_restore(local_device_id, local_genesis, contact);
        let tip_status = match restored_tip {
            Some(tip) if tip == [0u8; 32] => "zero",
            Some(_) => "restored",
            None => "missing",
        };
        log::info!(
            "[collect_tagged_inbox_addresses] contact={} tip_status={} device_prefix={}",
            contact.alias,
            tip_status,
            crate::util::text_id::encode_base32_crockford(
                &contact.device_id[..8.min(contact.device_id.len())]
            )
            .get(..8)
            .unwrap_or("?"),
        );

        let Some(current_tip) = restored_tip else {
            log::warn!(
                "[collect_tagged_inbox_addresses] contact {} has no valid canonical relationship tip",
                contact.alias
            );
            continue;
        };

        // 1. Current tip address (always)
        match crate::sdk::b0x_sdk::B0xSDK::compute_b0x_address(
            &local_genesis,
            &local_device_id,
            &current_tip,
        ) {
            Ok(addr) => addresses.push(TaggedInboxAddress {
                address: addr,
                freshness: RouteFreshness::Current,
            }),
            Err(e) => {
                log::warn!(
                    "[collect_tagged_inbox_addresses] contact {} current-tip address derivation failed: {}",
                    contact.alias,
                    e
                );
            }
        }

        // 2. Previous tip address (bounded lookback of 1)
        if let Some(prev_tip_bytes) = contact.previous_chain_tip.as_deref() {
            if prev_tip_bytes.len() == 32 && prev_tip_bytes != [0u8; 32] {
                let mut prev_tip = [0u8; 32];
                prev_tip.copy_from_slice(prev_tip_bytes);
                if prev_tip != current_tip {
                    match crate::sdk::b0x_sdk::B0xSDK::compute_b0x_address(
                        &local_genesis,
                        &local_device_id,
                        &prev_tip,
                    ) {
                        Ok(addr) => {
                            log::info!(
                                "[collect_tagged_inbox_addresses] contact {} stale-route address added (previous tip)",
                                contact.alias
                            );
                            addresses.push(TaggedInboxAddress {
                                address: addr,
                                freshness: RouteFreshness::PreviousTip,
                            });
                        }
                        Err(e) => {
                            log::warn!(
                                "[collect_tagged_inbox_addresses] contact {} previous-tip address derivation failed: {}",
                                contact.alias,
                                e
                            );
                        }
                    }
                }
            }
        }

        // 3. Pending-outbox next_tip address.
        //
        // When this device has a pending online outbox for `contact` (i.e. we sent
        // an online transfer that the counterparty hasn't yet ACKed), `record_pending_
        // online_transition` advances `contacts.local_bilateral_chain_tip` to T_{n+1}
        // without touching `current_chain_tip`. The counterparty sees our local tip as
        // T_{n+1} and will therefore deposit any reply using:
        //   compute_b0x_address(our_genesis, our_device_id, T_{n+1})
        //
        // But `current_chain_tip` still holds T_n, so address (1) above would miss it.
        // Adding the pending next_tip as a live `Current` address closes this gap.
        if contact.device_id.len() == 32 {
            if let Ok(Some(pending)) =
                crate::storage::client_db::get_pending_online_outbox(&contact.device_id)
            {
                if pending.next_tip.len() == 32 {
                    let mut next_tip = [0u8; 32];
                    next_tip.copy_from_slice(&pending.next_tip);
                    if next_tip != current_tip && next_tip != [0u8; 32] {
                        match crate::sdk::b0x_sdk::B0xSDK::compute_b0x_address(
                            &local_genesis,
                            &local_device_id,
                            &next_tip,
                        ) {
                            Ok(addr) => {
                                log::info!(
                                    "[collect_tagged_inbox_addresses] contact {} pending-outbox next-tip address added (T_n+1 reply window)",
                                    contact.alias
                                );
                                addresses.push(TaggedInboxAddress {
                                    address: addr,
                                    freshness: RouteFreshness::Current,
                                });
                            }
                            Err(e) => {
                                log::warn!(
                                    "[collect_tagged_inbox_addresses] contact {} pending-outbox next-tip address derivation failed: {}",
                                    contact.alias,
                                    e
                                );
                            }
                        }
                    }
                }
            }
        }
    }

    // Dedup by address: if current and previous collide, keep Current
    addresses.sort_by(|a, b| a.address.cmp(&b.address));
    addresses.dedup_by(|a, b| {
        if a.address == b.address {
            // Keep whichever is Current
            if a.freshness == RouteFreshness::Current {
                b.freshness = RouteFreshness::Current;
            }
            true
        } else {
            false
        }
    });
    addresses
}

/// Backward-compatible wrapper: returns plain address strings (current-tip only
/// behavior preserved for callers like `storage.sync` that auto-apply items).
pub(crate) fn collect_rotated_inbox_addresses(
    local_genesis: [u8; 32],
    local_device_id: [u8; 32],
    contacts: &[crate::storage::client_db::ContactRecord],
) -> Vec<String> {
    collect_tagged_inbox_addresses(local_genesis, local_device_id, contacts)
        .into_iter()
        .map(|t| t.address)
        .collect()
}

fn select_quorum_device_identity(
    candidates: impl IntoIterator<Item = QuorumDeviceIdentity>,
) -> Option<QuorumDeviceIdentity> {
    let mut counts: HashMap<([u8; 32], Vec<u8>), (usize, QuorumDeviceIdentity)> = HashMap::new();
    for candidate in candidates {
        let key = (candidate.genesis_hash, candidate.public_key.clone());
        let entry = counts.entry(key).or_insert((0, candidate.clone()));
        entry.0 += 1;
        if entry.0 >= 3 {
            return Some(entry.1.clone());
        }
    }
    None
}

async fn fetch_quorum_device_identity(
    storage_endpoints: &[String],
    device_id: [u8; 32],
) -> Result<Option<QuorumDeviceIdentity>, String> {
    if storage_endpoints.is_empty() {
        return Ok(None);
    }

    let client = crate::sdk::storage_node_sdk::build_ca_aware_client();
    let device_id_b32 = crate::util::text_id::encode_base32_crockford(&device_id);
    let mut candidates = Vec::new();
    let mut supported_response_seen = false;
    let mut last_error: Option<String> = None;

    for endpoint in storage_endpoints {
        let url = format!(
            "{}/api/v2/device/{}",
            endpoint.trim_end_matches('/'),
            device_id_b32
        );
        match client.get(&url).send().await {
            Ok(resp) if resp.status().is_success() => {
                supported_response_seen = true;
                let bytes = resp
                    .bytes()
                    .await
                    .map_err(|e| format!("device identity read failed: {e}"))?;
                let decoded = generated::RegisterDeviceRequest::decode(bytes.as_ref())
                    .map_err(|e| format!("device identity decode failed: {e}"))?;
                if decoded.device_id.as_slice() != device_id.as_slice() {
                    last_error = Some(format!("device identity mismatch from {}", endpoint));
                    continue;
                }
                let genesis_hash: [u8; 32] = match decoded.genesis_hash.as_slice().try_into() {
                    Ok(value) => value,
                    Err(_) => {
                        last_error = Some(format!(
                            "device identity from {} had invalid genesis length {}",
                            endpoint,
                            decoded.genesis_hash.len()
                        ));
                        continue;
                    }
                };
                if decoded.pubkey.is_empty() {
                    last_error = Some(format!(
                        "device identity from {} had empty pubkey",
                        endpoint
                    ));
                    continue;
                }
                candidates.push(QuorumDeviceIdentity {
                    device_id,
                    genesis_hash,
                    public_key: decoded.pubkey,
                });
            }
            Ok(resp)
                if resp.status() == reqwest::StatusCode::NOT_FOUND
                    || resp.status() == reqwest::StatusCode::BAD_REQUEST =>
            {
                // Older storage nodes do not expose this route yet; fall back to cached contact data.
            }
            Ok(resp)
                if resp.status() == reqwest::StatusCode::METHOD_NOT_ALLOWED
                    || resp.status() == reqwest::StatusCode::NOT_IMPLEMENTED =>
            {
                // Mixed-version replica set: sender cannot require lookup yet.
            }
            Ok(resp) => {
                last_error = Some(format!(
                    "device identity lookup HTTP {} from {}",
                    resp.status(),
                    endpoint
                ));
            }
            Err(e) => {
                last_error = Some(format!(
                    "device identity lookup failed for {}: {}",
                    endpoint, e
                ));
            }
        }
    }

    if let Some(identity) = select_quorum_device_identity(candidates) {
        return Ok(Some(identity));
    }

    if supported_response_seen {
        return Err(last_error.unwrap_or_else(|| {
            "device identity quorum unavailable across storage endpoints".to_string()
        }));
    }

    Ok(None)
}

