// Vault generation tests – all four languages
// ------------------------------------------------------------------

#[test]
fn test_vault_generation_rust() -> Result<(), Box<dyn std::error::Error>> {
    let spec = DsmSpecification::Vault(multi_sig_vault_spec());
    let generator = RustGenerator::new(true, true);
    let code = generator.generate(&spec)?;

    assert!(code.contains("auto-generated by dsm-gen"));
    assert!(code.contains("MultiSigVaultClient"));
    assert!(code.contains("enum FulfillmentCondition"));
    assert!(code.contains("BitcoinHtlc"));
    assert!(code.contains("RandomWalkVerification"));
    assert!(code.contains("VaultLifecycle"));
    assert!(code.contains("pub fn create_vault"));
    assert!(code.contains("pub fn unlock_vault"));
    assert!(code.contains("pub fn check_lifecycle"));
    // invariants
    assert!(
        !code.contains("duration_seconds"),
        "Invariant #4: no wall-clock"
    );
    assert!(!code.contains("TimeLock"), "Invariant #4: no TimeLock");
    assert!(!code.contains("serde_json"), "Invariant #2: no serde_json");
    assert!(
        !code.contains("lock_conditions"),
        "Invariant: old API absent"
    );
    Ok(())
}

